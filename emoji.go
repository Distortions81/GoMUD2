var emojiToName map[string]string

func init() {
	emojiToName = make(map[string]string)
	for i, item := range nameToEmoji {
		emojiToName[item] = i
	}
}

const MAX_EMOJI_NAME = 10

var emojiHelp, moreEmojiHelp string

func loadEmojiHelp() {

	var buf string
	var c int
	for item, emoji := range nameToEmoji {
		if len(item) < MAX_EMOJI_NAME {
			continue
		}
		if len(emoji) > 5 {
			continue
		}
		if strings.HasPrefix(item, "flag") {
			continue
		}
		if c%2 == 0 {
			buf = buf + "\r\n"
		}
		c++
		buf = buf + fmt.Sprintf(":%v: %-37v ", item, item)
	}
	buf = buf + "\r\nSimply chat :emoji name:\r\n"
	buf = buf + "These will show up as text to players using mud clients that do not support UTF."
	buf = buf + fmt.Sprintf("\r\n%v emoji. For more emoji see help emoji.", c)
	moreEmojiHelp = buf

	buf = ""
	c = 0
	for item, emoji := range nameToEmoji {
		if item == "registered" {
			continue
		}
		if len(item) >= MAX_EMOJI_NAME {
			continue
		}
		if len(emoji) > 5 {
			continue
		}
		if item == "copyright" {
			continue
		}
		if strings.HasPrefix(item, "flag") {
			continue
		}
		if c%5 == 0 {
			buf = buf + "\r\n"
		}
		c++
		buf = buf + fmt.Sprintf(":%v: %-11v ", item, item)
	}
	buf = buf + "\r\nSimply chat :emoji name:\r\n"
	buf = buf + "These will show up as text to players using mud clients that do not support UTF."
	buf = buf + fmt.Sprintf("\r\n%v emoji. For more emoji see help more-emoji.", c)
	emojiHelp = buf
}

func nameToUnicode(input string) string {
	var output string

	parts := strings.Split(input, ":")
	count := 0

	wasEmoji := false
	for _, item := range parts {
		symbol := nameToEmoji[item]

		if symbol != "" {
			output = output + symbol
			wasEmoji = true
		} else {
			if count == 0 || wasEmoji {
				output = output + item
			} else {
				output = output + ":" + item
			}
			wasEmoji = false
		}

		count++
	}

	return output
}

func unicodeToName(input string) string {
	var output string

	for _, item := range input {
		name := emojiToName[string(item)]
		if name != "" {
			output = output + ":" + name + ":"
		} else {
			output = output + string(item)
		}
	}

	return output
}

